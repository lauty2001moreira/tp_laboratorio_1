/** \brief  realiza la suma de dos numeros.
 *
 * \param float el primer numero a sumar.
 * \param float el segundo numero a sumar.
 * \return float retorna el valor de la suma
 *
 */
float sumar( float x , float y);

/** \brief resta realiza la resta de dos numeros
 *
 * \param float el primer numero a restar.
 * \param float el segundo numero a restar.
 * \return float retorna el valor de la resta
 *
 */
float restar( float x , float y);

/** \brief dividir realiza la divicion de dos numeros
 *
 * \param float el dividendo de la operacion.
 * \param float el divisor de la operacion.
 * \return float el resultado de la divicion.
 *
 */
float dividir( float x , float y);

/** \brief multiplicar realiza la multiplicacion de dos numeros
 *
 * \param float el multiplicando de la operacion.
 * \param float  el multiplicador de la operacion .
 * \return double retorna el valor de la multiplicacion
 *
 */
double multiplicar( float x , float y);

/** \brief factoriza un solo numero
 *
 * \param int el numero que se desea factorizar
 * \return long long int retorna el valor de la factorizacion o -1 si el numero ingresado es negativo.
 *
 */
long long int factorizar( int x );


